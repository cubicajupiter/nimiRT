#include "../../inc/libft.h"

double	ft_tan(double x)
{
	double	c;

	c = ft_cos(x);
	if (c > -1e-12 && c < 1e-12)
	{
		if (c < 0.0)
			return (-1.0 / 0.0);
		else
			return (1.0 / 0.0);
	}
	return (ft_sin(x) / c);
}
